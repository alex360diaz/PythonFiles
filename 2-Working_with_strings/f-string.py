# f-string
# modern, super-easy way to format and bouid strings
# "f" stands for "formatted"
# lets you easily put varibales and expressions
# directly inside string value
name = "Alex"
age = 27
is_student = False

print(
    f"My name is {name}, I am {age} years old, and my student status is {is_student}.")

# We can use f-string to use math
print(f"5 x 5 = {5 * 5}")

