# Types of numbers
x = 5
y = 5.7
z = 2 + 3j

print(type(x))
print(type(y))
print(type(z))

# Convert string to number --> we use int(name of the variable)

num = "10"
print(type(num))
print(num)
print(int(num) * 5)

# Convert float to int --> we use int(name of the variable)

num2 = 3.1416
print(type(num2))
print("Before convertion-->", num2)
print("After convertion-->", int(num2))

# We can convert anything int(), float(), str(), complex()